                                      <a href="single.html"><h1>How does the virus spread?</h1>
                                    </div>  
                                    <div class="post-info">
                                    	<span>April 22, 2020 / by <a href="#" target="_blank">Rejin Joseph/a></span>
                                    </div>  
                                    <p>The virus that causes COVID-19 spreads from person to person. Someone who has COVID-19 can spread the illness to others, even if they don’t show any symptoms. The principle mode of transmission is thought to occur mainly via respiratory droplets that travel up to six feet in the air after an infected person coughs or sneezes. This is similar to how influenza and other viruses spread. These droplets can land in the mouths or noses of people who are nearby or possibly be inhaled into the lungs.</p>
                                    <p>There is also some evidence that virus particles can remain on contaminated surfaces for several days, but the CDC does not believe that this is the main way that the virus spreads.</p>
                                    
