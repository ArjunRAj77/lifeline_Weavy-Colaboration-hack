                                    <div class="post-title">
                                      <h1>What should I do if I think I have COVID-19?</h1>
                                    </div>  
                                    <div class="post-info">
                                    	<span>April 22, 2020 / by <a href="#" target="_blank">Arjun Raj</a></span>
                                    </div>  
                                                                        <p>If you think you have been exposed to COVID-19 and develop a fever and symptoms such as cough or difficulty breathing, call your health care provider or our patient hotline at 415-514-7328. Please use the UCSF MyChart “Coronavirus &amp; Flu Symptom Checker” before you call the hotline. If you are a patient of the UCSF Cancer Center, please contact that clinic for specific instructions prior to calling the UCSF coronavirus line.</p>
                                    <p>If you are a UCSF Health patient and have symptoms of COVID-19, we are committed to helping you get tested within 48 hours.</p>
                                    <p>In the meantime, if you feel sick, take the following precautions:</p>
                                    <ul><li>Avoid public areas, and stay away from others in your home. If you can use a separate bedroom and bathroom, do so.</li>
                                      <li>Cover your mouth and nose when coughing or sneezing.</li>
                                      <li>Wear a mask to cover your nose and mouth if you have cough and are around other people in your home or in public. Please do not wear a mask with a valve, as these masks allow droplet release and do not protect others who may be nearby.</li>
                                      <li>Wash your hands after touching your face, before eating, after using the bathroom. If soap and water is not available, use hand sanitizer with &gt;60% alcohol.</li>
                                      <li>Avoid sharing personal household items.</li>
                                      <li>Clean all high touch services every day such as counters, tabletops, doorknobs, phones, keyboards. Use a household cleaning spray or wipe according to label instructions.</li>
                                      <li>Monitor your symptoms. If you feel your symptoms are worsening, contact your health care provider.</li>
                                    </ul><p><a href="https://www.my-emmi.com/eplayer/en/?viewId=DF55B207-EA74-49C8-BE2D-2CDB70F3EB38&amp;video=true&amp;src=emmi&amp;locale=en_US&amp;ccVersion=false" target="_blank">Watch a video that explains more.</a></p>
                                    <p>If you have a medical emergency, call 911 and notify the dispatch personnel that you have or are being evaluated for COVID-19.</p>
                                    
