                                    <p>The 2019 novel coronavirus was identified as the cause of an outbreak of respiratory illness first detected in Wuhan City, Hubei Province, China, in December 2019. The virus has been named SARS-CoV-2, and the disease it causes has been named coronavirus disease 2019, abbreviated as COVID-19. On March 11, 2020, the COVID-19 outbreak was declared a pandemic by the World Health Organization.</p>
